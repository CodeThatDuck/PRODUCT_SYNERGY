-- Table: DEPARTMENTS
CREATE TABLE DEPARTMENTS (
    DEPT_ID NUMBER(10) NOT NULL,
    DEPT_NAME VARCHAR2(100) NOT NULL,
    DEPT_CODE VARCHAR2(10) NOT NULL,
    FLOOR_NUMBER NUMBER(3),
    PHONE_EXT VARCHAR2(10),
    BUDGET NUMBER(15,2),
    CONSTRAINT PK_DEPARTMENTS PRIMARY KEY (DEPT_ID)
);

-- Table: DOCTORS
CREATE TABLE DOCTORS (
    DOCTOR_ID NUMBER(10) NOT NULL,
    FIRST_NAME VARCHAR2(100) NOT NULL,
    LAST_NAME VARCHAR2(100) NOT NULL,
    SPECIALIZATION VARCHAR2(150) NOT NULL,
    LICENSE_NUMBER VARCHAR2(50) NOT NULL,
    DEPT_ID NUMBER(10) NOT NULL,
    HIRE_DATE DATE NOT NULL,
    SALARY NUMBER(12,2),
    EMAIL VARCHAR2(255),
    CONSTRAINT PK_DOCTORS PRIMARY KEY (DOCTOR_ID)
);

ALTER TABLE DOCTORS
    ADD CONSTRAINT FK_DOCTORS_DEPT_ID
    FOREIGN KEY (DEPT_ID)
    REFERENCES DEPARTMENTS(DEPT_ID);

CREATE INDEX IDX_DOCTORS_DEPT_ID ON DOCTORS(DEPT_ID);

-- Table: PATIENTS
CREATE TABLE PATIENTS (
    PATIENT_ID NUMBER(10) NOT NULL,
    FIRST_NAME VARCHAR2(100) NOT NULL,
    LAST_NAME VARCHAR2(100) NOT NULL,
    DATE_OF_BIRTH DATE NOT NULL,
    GENDER CHAR(1) NOT NULL,
    BLOOD_TYPE VARCHAR2(5),
    PHONE VARCHAR2(20),
    EMAIL VARCHAR2(255),
    ADDRESS VARCHAR2(500),
    INSURANCE_ID VARCHAR2(50),
    REGISTRATION_DATE DATE NOT NULL,
    CONSTRAINT PK_PATIENTS PRIMARY KEY (PATIENT_ID)
);

-- Table: APPOINTMENTS
CREATE TABLE APPOINTMENTS (
    APPOINTMENT_ID NUMBER(10) NOT NULL,
    PATIENT_ID NUMBER(10) NOT NULL,
    DOCTOR_ID NUMBER(10) NOT NULL,
    APPOINTMENT_DATE DATE NOT NULL,
    APPOINTMENT_TIME VARCHAR2(10) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL,
    REASON VARCHAR2(500),
    NOTES CLOB,
    CONSTRAINT PK_APPOINTMENTS PRIMARY KEY (APPOINTMENT_ID)
);

ALTER TABLE APPOINTMENTS
    ADD CONSTRAINT FK_APPT_PATIENT_ID
    FOREIGN KEY (PATIENT_ID)
    REFERENCES PATIENTS(PATIENT_ID);

ALTER TABLE APPOINTMENTS
    ADD CONSTRAINT FK_APPT_DOCTOR_ID
    FOREIGN KEY (DOCTOR_ID)
    REFERENCES DOCTORS(DOCTOR_ID);

CREATE INDEX IDX_APPT_PATIENT_ID ON APPOINTMENTS(PATIENT_ID);
CREATE INDEX IDX_APPT_DOCTOR_ID ON APPOINTMENTS(DOCTOR_ID);

-- Table: MEDICATIONS
CREATE TABLE MEDICATIONS (
    MED_ID NUMBER(10) NOT NULL,
    MED_NAME VARCHAR2(200) NOT NULL,
    GENERIC_NAME VARCHAR2(200),
    CATEGORY VARCHAR2(100) NOT NULL,
    UNIT_PRICE NUMBER(10,2) NOT NULL,
    STOCK_QUANTITY NUMBER(10) NOT NULL,
    MANUFACTURER VARCHAR2(200),
    CONSTRAINT PK_MEDICATIONS PRIMARY KEY (MED_ID)
);

-- Table: PRESCRIPTIONS
CREATE TABLE PRESCRIPTIONS (
    PRESCRIPTION_ID NUMBER(10) NOT NULL,
    APPOINTMENT_ID NUMBER(10) NOT NULL,
    MED_ID NUMBER(10) NOT NULL,
    DOSAGE VARCHAR2(100) NOT NULL,
    FREQUENCY VARCHAR2(100) NOT NULL,
    DURATION_DAYS NUMBER(5) NOT NULL,
    QUANTITY_PRESCRIBED NUMBER(5) NOT NULL,
    TOTAL_COST NUMBER(10,2),
    CONSTRAINT PK_PRESCRIPTIONS PRIMARY KEY (PRESCRIPTION_ID)
);

ALTER TABLE PRESCRIPTIONS
    ADD CONSTRAINT FK_PRESC_APPOINTMENT_ID
    FOREIGN KEY (APPOINTMENT_ID)
    REFERENCES APPOINTMENTS(APPOINTMENT_ID);

ALTER TABLE PRESCRIPTIONS
    ADD CONSTRAINT FK_PRESC_MED_ID
    FOREIGN KEY (MED_ID)
    REFERENCES MEDICATIONS(MED_ID);

CREATE INDEX IDX_PRESC_APPOINTMENT_ID ON PRESCRIPTIONS(APPOINTMENT_ID);
CREATE INDEX IDX_PRESC_MED_ID ON PRESCRIPTIONS(MED_ID);

-- Table: LAB_TESTS
CREATE TABLE LAB_TESTS (
    TEST_ID NUMBER(10) NOT NULL,
    TEST_NAME VARCHAR2(200) NOT NULL,
    TEST_CODE VARCHAR2(20) NOT NULL,
    CATEGORY VARCHAR2(100),
    NORMAL_RANGE VARCHAR2(100),
    UNIT VARCHAR2(50),
    COST NUMBER(10,2) NOT NULL,
    CONSTRAINT PK_LAB_TESTS PRIMARY KEY (TEST_ID)
);

-- Table: TEST_RESULTS
CREATE TABLE TEST_RESULTS (
    RESULT_ID NUMBER(10) NOT NULL,
    APPOINTMENT_ID NUMBER(10) NOT NULL,
    TEST_ID NUMBER(10) NOT NULL,
    RESULT_VALUE VARCHAR2(200),
    RESULT_DATE DATE NOT NULL,
    IS_ABNORMAL CHAR(1),
    TECHNICIAN_NOTES VARCHAR2(500),
    CONSTRAINT PK_TEST_RESULTS PRIMARY KEY (RESULT_ID)
);

ALTER TABLE TEST_RESULTS
    ADD CONSTRAINT FK_RESULT_APPOINTMENT_ID
    FOREIGN KEY (APPOINTMENT_ID)
    REFERENCES APPOINTMENTS(APPOINTMENT_ID);

ALTER TABLE TEST_RESULTS
    ADD CONSTRAINT FK_RESULT_TEST_ID
    FOREIGN KEY (TEST_ID)
    REFERENCES LAB_TESTS(TEST_ID);

CREATE INDEX IDX_RESULT_APPOINTMENT_ID ON TEST_RESULTS(APPOINTMENT_ID);
CREATE INDEX IDX_RESULT_TEST_ID ON TEST_RESULTS(TEST_ID);

